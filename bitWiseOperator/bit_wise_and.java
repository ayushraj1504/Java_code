package Java.bitWiseOperator;

public class bit_wise_and {
    public static void main(String[] args) {
        System.out.println((5 & 6));
        System.out.println((5|6));
        System.out.println((5^6));
        System.out.println((~7));
        System.out.println((~0));
        System.out.println((5<<2));
        System.out.println((6>>1));
    }
}
